package practicepackage;

public class DataTypeFloat {

    public static void main(String[] args) {

        float R = 9;
        float S = 11;

        System.out.println( R + S );
        System.out.println( R - S );
        System.out.println( R * S );
        System.out.println( R / S );
        System.out.println( R ++ );
        System.out.println( R -- );

    }
}
