package practicepackage;

public class DataTypeInteger {

    public static void main(String[] args) {

        int R = 18;
        int S = 6;

        System.out.println( R + S );
        System.out.println( R - S );
        System.out.println( R * S );
        System.out.println( R / S );
        System.out.println( R ++ );
        System.out.println( R -- );

    }
}
